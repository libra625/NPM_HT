const add = function (number1, number2) {
  return number1 + number2;
};

const minus = function (number1, number2) {
  return number1 - number2;
};

const multiply = function (number1, number2) {
  return number1 * number2;
};

console.log(add(5, 8));
console.log(minus(5, 8));
console.log(multiply(5, 8));
